public class VariableSwap15 {
    //Write a Java program to swap two variables.
    public static void main(String[] args){

        String a, b, temp;
        a = "RIDDHI"; //string variable defined
        b = "SHAH";  //string variable defined
        System.out.println("Before swapping : a, b = "+a+", "+b);// print statement
        temp = a; //value assigned to variable temp
        a = b; //value assigned to variable a
        b = temp; //value assigned to variable b
        System.out.println("After swapping : a, b = "+a+", "+b );//print statement
    }
}













