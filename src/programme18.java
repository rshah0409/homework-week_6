public class programme18 {
    /*Write a Java program to print the sum (addition), multiply, subtract, divide and  remainder of two numbers.
Test Data:
Input first number: 125
Input second number: 24
Expected Output :
125 + 24 = 149
125 - 24 = 101
125 x 24 = 3000
125 / 24 = 5
125 mod 24 = 5 */

    public static void main(String[] args){

         int a,b,sum,multiply,subtract,divide,remainder;//data type defined
         a = 125;//value defined for variable a
         b = 24; //value defined for variable b
         sum = a + b; //add a and b
         multiply = a * b;//multiply a and b
         subtract = a - b;//subtract a and b
         divide = a / b;//divide a and b
         remainder = a % b;//find remainder of a and b
         System.out.println("125 + 24 = "+sum);
         System.out.println("125 - 24 = "+subtract);
         System.out.println("125 x 24 = "+multiply);
         System.out.println("125 / 24 = "+divide);
         System.out.println("125 mod 24 = "+remainder);










    }









}
